# Arithmetic Operators
#
#   +     Addition
#   -     Subtraction
#   *     Multiplication
#   /     Division
#   %     Modulus
#   //    Floor Division
#   **    Exponentiation
#

x = 7
y = 2

print('x + y = ', x + y)
print('x * y = ', x * y)
print('x / y = ', x / y)
print('x % y = ', x % y)
print('x // y = ', x // y)
print('x ** y =', x ** y)

# ** has highest priority in arithmetic set of operators
print(-4 ** 2)  # -16
print((-4) ** 2)  # 16
